def rocket_add(a, b):
    """ Adds two numbers with rocket speed """
    return a + b

def ghost_sub(a, b):
    """ Subtracts b from a like a ghost """
    return a - b

def alien_mul(a, b):
    """ Mutiplies numbers using alien technology """
    return a * b

def poop_div(a, b):
    """ Divides numbers... messily """
    if b == 0: return 0
    return a // b
